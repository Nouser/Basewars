function DermaHelpTab(tbl, name, prettyname, icon)
	local name = vgui.Create("DPanelList")
	name:EnableHorizontal(false)
	name:EnableVerticalScrollbar(true)
	name:SetPadding(5)
	name:SetSpacing(5)
	name:SetSize((PropertySheet:GetWide() - 10), PropertySheet:GetTall())
	name:SetPos(0, 0)

	for k,v in pairs(tbl) do
		local cat = vgui.Create("DCollapsibleCategory") 
		cat:SetLabel(v[1])
		local list = vgui.Create("DPanelList")
		list:SetPadding(10)
		list:SetDrawBackground( false )
		for k2,v2 in pairs(v) do
			if k2 != 1 then
				local lable = vgui.Create("DLabel", list)
				lable:SetText(v2)
				lable:SizeToContents()
				list:AddItem(lable)
			end
		end
		list:SetSize(680, 14 * #v)
		cat:SetContents(list)
		cat:SizeToContents()
		cat:InvalidateLayout()
		if k != 1 then
			cat:SetExpanded(false)
		end
		name:AddItem(cat)
	end
	PropertySheet:AddSheet( prettyname, name, icon, false, false, prettyname )
end

